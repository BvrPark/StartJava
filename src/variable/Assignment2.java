package variable;

public class Assignment2 {
	
	public static void main(String[] args) {
		long a = 20;
		int b = (int)a;
		
		char c = 65;
		int d = c - 'A';
		
		float e = 15.4f;
		int f = (int)e;
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		
	}

}
